;HARSHIT 19BCE0382
JMP START
DATACW   DB 0000_0110B
         DB 0000_0100B    
         DB 0000_0011B
         DB 0000_0010B    
;DATACW: BIN DATA
;CLOCKWISE HALF STEP ROTATION
START: MOV BX, OFFSET DATACW;START FROM CLOCKWISE
MOV SI, 0
NEXT_STEP:;MOTOR SETS TOP BIT WHEN IT'S READY
WAIT: IN AL, 07H
TEST AL, 10000000B
JZ WAIT
MOV AL, [BX][SI]
OUT 7, AL
INC SI   ;INCREASE COUNTER
CMP SI, 4
JC NEXT_STEP
MOV SI, 0
JMP NEXT_STEP